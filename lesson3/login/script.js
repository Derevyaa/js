window.onload = function () {
  // Запит імені користувача через prompt
  let userName = prompt('Введіть ваше ім\'я:', 'Гість');

  // Підтвердження введеного імені через confirm
  if (confirm('Ваше ім\'я ' + userName + '? Натисніть OK для підтвердження або Cancel для скасування.')) {
    // Якщо користувач підтверджує, виводиться привітання через alert
    alert('Ласкаво просимо, ' + userName + '!');
  } else {
    // Якщо користувач скасовує, знову запитується ім'я
    userName = prompt('Введіть ваше ім\'я знову:', 'Гість');
    alert('Дякуємо, ' + userName);
  }
};
